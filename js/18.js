// Operador de comparacion

const numero1 = 20;
const numero2 = "20";

if (numero1 == numero2) {
  console.log("Si son iguales");
} else {
  console.log("No, no son iguales");
}

if (numero1 === numero2) {
  console.log("Si son iguales");
} else {
  console.log("No, no son iguales");
}

console.log("typeof numero1", typeof numero1);
console.log("typeof numero2", typeof numero2);
